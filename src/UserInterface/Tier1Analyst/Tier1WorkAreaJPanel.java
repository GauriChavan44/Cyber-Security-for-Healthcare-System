    private void btnViewRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewRequestActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSecutiryThreats.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Select a Row !", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        WorkRequest workRequest = (UnusualActivityWorkRequest) tblSecutiryThreats.getValueAt(selectedRow, 0);
        if(workRequest.getStatus().equals("Sent")){
            ViewSecurityThreatJPanel viewSecurityThreatJPanel = new ViewSecurityThreatJPanel(userProcessContainer, (UnusualActivityWorkRequest) workRequest,userAccount);
            userProcessContainer.add("viewSecurityThreatJPanel",viewSecurityThreatJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else if(workRequest.getStatus().equals("Request Escalated")){
            JOptionPane.showMessageDialog(null, "Request has been Already been Escalated !", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        else{
            JOptionPane.showMessageDialog(null, "Request Already Under Review !", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnViewRequestActionPerformed

    private void btnEscalateRequestsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEscalateRequestsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSecutiryThreats.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "First Select a Request !", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        WorkRequest request = (UnusualActivityWorkRequest) tblSecutiryThreats.getValueAt(selectedRow, 0);
        
        
        if(!request.getStatus().equals("Request Escalated")){
            Boolean tier2AnalystExist=false;
            if(request.getStatus().equals("Reviewed")){
                for(UserAccount ua:securityOrganization.getUserAccountDirectory().getUserAccountList()){
                    if(String.valueOf(ua.getRole()).equals(RoleType.Tier2Analyst.getValue())){
                        ua.getWorkQueue().getWorkRequestList().add(request);
                        tier2AnalystExist=true;
                        break;
                    }
                }
                if(tier2AnalystExist==true){
                    request.setStatus("Request Escalated");
                    populateSecurityThreatsTable();
                    JOptionPane.showMessageDialog(null, "Work Request Escalated to Tier 2 !", "Information", JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Tier2 Analysts do not Exist !", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "First Set the Severity !", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Request has been Already been Escalated !", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_btnEscalateRequestsActionPerformed

    private void btnResolveThreatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResolveThreatActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSecutiryThreats.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "First Select a Request !", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        UnusualActivityWorkRequest request = (UnusualActivityWorkRequest) tblSecutiryThreats.getValueAt(selectedRow, 0);
        
        Boolean checkSOCAnalysisExist=false;
        if(request.getStatus().equals("Reviewed")){
            if(request.getSeverity()<2 ){
                for(UserAccount ua:securityOrganization.getUserAccountDirectory().getUserAccountList()){
                    if(String.valueOf(ua.getRole()).equals(RoleType.SOCAnalysisTeam.getValue())){
                        ua.getWorkQueue().getWorkRequestList().add(request);
                        checkSOCAnalysisExist=true;
                    }
                }
                if(checkSOCAnalysisExist==true){
                    JOptionPane.showMessageDialog(null, "Work Request Escalated to SOC Analysis Team !", "Information", JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null, "SOC Analysis Team do not Exist !", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Escalate Work Request to Tier 2 Analyst", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
        }
        else if(request.getStatus().equals("Request Escalated")){
            JOptionPane.showMessageDialog(null, "Request has been Already been Escalated !", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        else{
            JOptionPane.showMessageDialog(null, "Request is not under reciew !", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnResolveThreatActionPerformed

