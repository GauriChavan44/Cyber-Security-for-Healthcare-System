    public void populateSecurityThreatTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblSecutiryThreats.getModel();
        dtm.setRowCount(0);
        
        Tier1Analyst t1 = (Tier1Analyst) userAccount.getPerson();
        for(WorkRequest work:t1.getWorkQueue().getWorkRequestList()){
            if(work instanceof UnusualActivityWorkRequest){
                
                Object row[]=new Object[5];
                row[0]=work;//.getWorkRequestId();
                row[1]=((UnusualActivityWorkRequest) work).getThreatUser().getPerson().getFirstName();   //work.getThreatUser().getPerson().getFirstName();
                row[2]=((UnusualActivityWorkRequest) work).getThreatUser().getPerson().getLastName();
                row[3]=work.getMessage();
                row[4]=work.getStatus();
                
                dtm.addRow(row);
            }
        }
    }
    
